package com.smileflowpig.money.factory.bean;

public class Amount {


        private String await_amount;
        private String all_amount;
        private String already_amount;

        public String getAwait_amount() {
            return await_amount;
        }

        public void setAwait_amount(String await_amount) {
            this.await_amount = await_amount;
        }

        public String getAll_amount() {
            return all_amount;
        }

        public void setAll_amount(String all_amount) {
            this.all_amount = all_amount;
        }

        public String getAlready_amount() {
            return already_amount;
        }

        public void setAlready_amount(String already_amount) {
            this.already_amount = already_amount;
        }
}

